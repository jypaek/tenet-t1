/**
 * @arg imageData : image data from cyclops
 * @arg format    : BW or Color image (0=BW, 1=Color)
 * @arg width     : width of image
 * @arg height    : height of image
 * @arg filename  : filename of the output bmp file
 **/
 
int convert_data_to_bmp(char *imageData, int format, int width, int height, char *filename) {
    FILE *fp;     // file pointer of the output bmp file
    int i, i2;
    char tmp;
    HEADER h1;
    INFOHEADER h2;
    int align = 0;  // needed for both types of images

    // Open a file to save data
    if((fp = fopen (filename, "wb")) == NULL) {
        printf("Ouput Data file error\n");
        exit(1);
    }

    /********Black/White image*********/
    if (format == 0) {
        align = ((height*width)+54) % 4;
        // size of 'imageData'  must be  (width*height);

        h1.type = 19778; //'M' * 256 + 'B';
        h1.size = (width*height) + 54 + align;
        h1.reserved1 = 0;
        h1.reserved2 = 0;
        h1.offset = 54+256*4;

        h2.size = 40;
        h2.width = width;
        h2.height = height;
        h2.planes = 1;
        h2.bits = 8;
        h2.compression = 0;
        h2.imagesize = 0;
        h2.xresolution = 0;
        h2.yresolution = 0;
        h2.ncolours = 256;
        h2.importantcolours = 0;
    }

    /************COLOR image***********/
    if (format == 1) {
        align = ((width*height*3) + 54) % 4;
        // size of 'imageData'  must be  (width*height*3);

        h1.type = 19778; //'M' * 256 + 'B';
        h1.size = (width*height*3) + 54 + align;
        h1.reserved1 = 0;
        h1.reserved2 = 0;
        h1.offset = 54;

        h2.size = 40;
        h2.width = width;
        h2.height = height;
        h2.planes = 1;
        h2.bits = 24;
        h2.compression = 0;
        h2.imagesize = 0;
        h2.xresolution = 0;
        h2.yresolution = 0;
        h2.ncolours = 0;
        h2.importantcolours = 0;
        i2 = 1;
        for (i = 0; i < width*height*3; i++) {
            if (i2%3 == 0) {
                tmp = imageData[i];
                imageData[i] = imageData[i-2];
                imageData[i-2] = tmp;
            }
            i2++;
        }
    }

    fwrite (&h1, sizeof(HEADER), 1, fp);
    fwrite (&h2, sizeof(INFOHEADER), 1, fp);

    if (format == 0) { //Insert Color Pallete only if BW image 
        fwrite (palette, sizeof(char), 256*4, fp); 
        fwrite (imageData, sizeof(char), width*height, fp);
    }

    if (format == 1)
        fwrite (imageData, sizeof(char), width*height*3, fp);

    //free (imageData); // don't free it here
    fclose(fp);
    return 0;
}
 
int convert_bmp_to_dat(char *filename, int *f, int *w, int *h, char **returnBuf) {
    FILE *fp;     // file pointer of the output bmp file
    HEADER h1;
    INFOHEADER h2;
    unsigned char p1[256*4];
    int width, height, format;
    char *imageData;
    int len, ret = -1;
    int currChar;
    int i = 0;

    // Open a bmp file to read
    if ((fp = fopen (filename, "rb")) == NULL) {
        printf("Input Data file error\n");
        exit(1);
    }

    fread (&h1, sizeof(HEADER), 1, fp);
    fread (&h2, sizeof(INFOHEADER), 1, fp);

    width = h2.width;
    height = h2.height;
    format = ((h2.bits/8)-1)/2;

    if (format == 1) {  // COLOR
        len = width * height * 3;
    } else if (format == 0) {
        len = width * height;
    } else {
        printf("Wrong format !!\n");
        exit(1);
    }

    if (format == 0)    // color pallete inserted only if BW image 
        fread (p1, sizeof(char), 256*4, fp); 

    imageData = (char *) malloc(len);

    i = 0;

    /* read input until there's nothing left */
    while ((currChar = fgetc(fp)) != EOF) {
        if (i < len) {
            imageData[i] = (char)currChar;

            /* if COLOR image */
            if (format == 1) {
                char tmp;
                if ((i+1) % 3 == 0) {
                    tmp = imageData[i];
                    imageData[i] = imageData[i-2];
                    imageData[i-2] = tmp;
                }
            }
        }
        i++;
    }
    if (i == len)
        ret = len;
    else if (i > len)
        printf("File length is longer than expected? %d > %d = %d * %d * %d\n", i, len, width, height, format*2+1);
    else if (i < len)
        printf("File length is shorter than expected? %d < %d = %d * %d * %d\n", i, len, width, height, format*2+1);

    fclose(fp);

    *returnBuf = imageData;
    *f = format;
    *w = width;
    *h = height;
    return ret;
}

